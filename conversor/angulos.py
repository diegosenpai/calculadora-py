import math

def convertirGradosARadianes(valorEnGrados):
    """ Permite transformar grados a radianes


    Args:
        valorEnGrados (float): valor en grados

    Returns:
        float: valor en radianes
    """
    return math.radians(valorEnGrados)

def convertirRadianesAGrados(valorEnRadianes):
    """Permite convertir radianes a grados

    Args:
        valorEnRadianes (float): valor en radianes

    Returns:
        float: valor en grados
    """
    return math.degrees(valorEnRadianes)

